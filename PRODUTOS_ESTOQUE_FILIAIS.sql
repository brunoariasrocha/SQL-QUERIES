SELECT
	SB2.B2_FILIAL,
	SB2.B2_QATU,
	SB2.B2_COD, 
	SB1.B1_DESC,
	SB1.B1_CODBAR,
	SB1.B1_XCAT1 AS CATEGORIA,
	SB1.B1_XCAT2 AS SUBGRUPO1,
	SB1.B1_XGRP1 AS SUBGRUPO2,
	SB1.B1_XGRP2 AS SUBGRUPO3
FROM [1_RAW].[SB2_PROTHEUS] SB2
LEFT JOIN [1_RAW].[SB1_PROTHEUS] SB1
	ON SB2.B2_COD = SB1.B1_COD
WHERE SB2.B2_QATU > 0
  AND SB2.B2_FILIAL = '01011007'
  AND (
        LTRIM(RTRIM(SB1.B1_CODBAR)) <> ''
        AND SB1.B1_CODBAR IS NOT NULL
      )
ORDER BY B2_FILIAL
