SELECT
    CDL2.REGIONAL_GERENCIAL,
    CDL2.FILIAL_EXT,
    CDL1.COD_PRODUTO,
    CDL1.DSC_PRODUTO,
    CDL1.STATUS_PRODUTO
FROM [3_CUR].[TBL_DIM_PRODUTOS_ORIG_CDL] AS CDL1
LEFT JOIN [3_CUR].[DIM_FILIAIS] AS CDL2
    ON CDL1.CNPJ_EMPRESA = CDL2.CNPJ
WHERE CDL1.COD_PRODUTO IN (
    17143, 17145, 17148, 17730, 17731, 17997, 18046, 18110, 18122, 18123, 
    18124, 18354, 18356, 18485, 19657, 19660, 19661, 19662, 19663, 19680, 
    20598, 20773, 20844, 20889, 21590, 22031, 22278, 22279, 22280, 22281, 
    22282, 22284, 26057, 26173, 26174, 26769, 45090, 45091, 45096, 45098, 
    45124, 45129, 45149, 45151, 45152, 45157, 45192, 45197, 45226, 45235, 
    45238, 45244, 50263, 50270, 50271, 54147, 71869, 71922, 71945, 99999
)
AND (CDL2.REGIONAL_GERENCIAL = 'Tangará Da Serra' OR CDL2.REGIONAL_GERENCIAL = 'Ariquemes')
ORDER BY CDL2.REGIONAL_GERENCIAL;
